/**
 * Sort an array alphabetically (ascending)
 * Syntax: array.sort()
 */
const sortArray = ["Nguyen","Dinh","Trong","PTIT"];
sortArray.sort();
console.log(sortArray);
// document.getElementById("sort").innerHTML = sortArray; // Dinh,Nguyen,PTIT,Trong

/**
 * Reverse array (descending)
 */
const sortArray2 = ["Nguyen","Dinh","Trong","PTIT"];
sortArray2.sort();
sortArray2.reverse();
console.log(sortArray2);
// document.getElementById("sort2").innerHTML = sortArray2; // Trong,PTIT,Nguyen,Dinh
